class FurnitureEntity {
  final int id;
  final String title;
  final String description;
  final String imgUrl;
  final String category;
  final String price;
  final String userId;

  FurnitureEntity({
    required this.id,
    required this.title,
    required this.description,
    required this.imgUrl,
    required this.category,
    required this.price,
    required this.userId,
  });
}
