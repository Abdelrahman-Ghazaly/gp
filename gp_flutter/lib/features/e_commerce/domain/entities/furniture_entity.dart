class FurnitureEntity {
  final int id;
  final String title;
  final String description;
  final String imageUrl;
  final String category;
  final String price;
  final String sellerId;

  FurnitureEntity({
    required this.id,
    required this.title,
    required this.description,
    required this.imageUrl,
    required this.category,
    required this.price,
    required this.sellerId,
  });
}
